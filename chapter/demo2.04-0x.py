
#输出二进制
print(0b001100)
#输出八进制
print(0o123)
#输出十六进制数
print(0xF15)

#十进制转换二进制
print(bin(12))
#二进制转十进制
print(int('10110',2))

#十六进制转十进制
print(int("0xF35AE",16))
#十进制转十六进制
print(hex(54321))

#十六进制转二进制
print(bin(0xF12E))
#二进制转十六进制
print(hex(0b1101101))

#十进制转八进制
print(oct(1234))
#八进制转十进制
print(int("76532",8))

